        [Fact]
        public async Task ProcessCsvFile_ValidFile_ProcessesSuccessfully()
        {
            // Arrange
            var mockRepository = new Mock<IDatabaseRepository>();
            var csvProcessingService = new CsvProcessingService(mockRepository.Object);

            var csvContent = @"caller_id,recipient,call_date,end_time,duration,cost,reference,currency
                           441215598896,448000096481,16/08/2016,14:21:33,43,0,C5DA9724701EEBBA95CA2CC5617BA93E4,GBP";
